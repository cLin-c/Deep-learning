def train(hyp, opt, device, callbacks):
    # 初始化保存目录、日志记录器等
    save_dir, epochs, batch_size, weights, single_cls, evolve, data, cfg, resume, noval, nosave, workers, freeze = \
        Path(opt.save_dir), opt.epochs, opt.batch_size, opt.weights, opt.single_cls, opt.evolve, opt.data, opt.cfg, \
        opt.resume, opt.noval, opt.nosave, opt.workers, opt.freeze
    callbacks.run('on_pretrain_routine_start')

    # 加载数据集
    data_dict = check_dataset(data)
    train_path, val_path = data_dict['train'], data_dict['val']
    nc = 1 if single_cls else int(data_dict['nc'])
    names = {0: 'item'} if single_cls and len(data_dict['names']) != 1 else data_dict['names']

    # 检查 hyp 是否为字符串（文件路径），并加载超参数
    if isinstance(hyp, str):
        with open(hyp, errors='ignore') as f:
            hyp = yaml.safe_load(f)  # 加载 YAML 文件为字典
    elif not isinstance(hyp, dict):
        raise ValueError("hyp must be a dictionary or a path to a YAML file")

    # 初始化学生模型和教师模型
    student_model = Model(cfg, ch=3, nc=nc, anchors=hyp.get('anchors')).to(device)
    teacher_model = Model(cfg, ch=3, nc=nc, anchors=hyp.get('anchors')).to(device)
    teacher_model.load_state_dict(student_model.state_dict())  # 初始化教师模型为学生模型的初始状态
    for param in teacher_model.parameters():
        param.requires_grad = False  # 教师模型参数固定

    # 计算网格大小（最大步幅）
    gs = max(int(student_model.stride.max()), 32)  # grid size (max stride)
    unlabeled_path = "/home/user/ccl/yolov9/data/NEU-DET/test_split/images/"
    # 数据加载器（标注数据和未标注数据）
    train_loader, dataset = create_dataloader(train_path, opt.imgsz, batch_size // WORLD_SIZE, gs, single_cls, hyp=hyp, augment=True, cache=opt.cache, rect=opt.rect, rank=LOCAL_RANK, workers=workers, prefix=colorstr('train: '))
    unlabeled_loader = create_dataloader(unlabeled_path, opt.imgsz, batch_size // WORLD_SIZE, gs, single_cls, hyp=hyp, augment=True, cache=opt.cache, rect=opt.rect, rank=LOCAL_RANK, workers=workers, prefix=colorstr('unlabeled: '))  # 未标注数据加载器

    # 优化器和学习率调度器
    optimizer = smart_optimizer(student_model, opt.optimizer, hyp['lr0'], hyp['momentum'], hyp['weight_decay'])
    scheduler = lr_scheduler.LambdaLR(optimizer, lr_lambda=lambda x: (1 - x / epochs) * (1.0 - hyp['lrf']) + hyp['lrf'])

    # 损失函数
    compute_loss = ComputeLoss(student_model)
    consistency_loss_fn = nn.MSELoss()  # 一致性损失函数

    # 混合精度训练
    amp = check_amp(student_model)
    scaler = torch.cuda.amp.GradScaler(enabled=amp)

    # 开始训练
    for epoch in range(epochs):
        student_model.train()
        teacher_model.eval()

        # 标注数据训练
        for imgs, targets, paths, _ in train_loader:
            imgs = imgs.to(device, non_blocking=True).float() / 255
            targets = targets.to(device)

            with torch.cuda.amp.autocast(amp):
                pred = student_model(imgs)
                loss, loss_items = compute_loss(pred, targets)  # 监督损失

            scaler.scale(loss).backward()
            scaler.step(optimizer)
            scaler.update()
            optimizer.zero_grad()

        # 未标注数据训练
        for imgs, _, paths, _ in unlabeled_loader:
            imgs = imgs.to(device, non_blocking=True).float() / 255

            with torch.no_grad():
                with torch.cuda.amp.autocast(amp):
                    teacher_pred = teacher_model(imgs)  # 教师模型生成伪标签

            with torch.cuda.amp.autocast(amp):
                student_pred = student_model(imgs)
                consistency_loss = consistency_loss_fn(student_pred, teacher_pred.detach())  # 一致性损失

            scaler.scale(consistency_loss).backward()
            scaler.step(optimizer)
            scaler.update()
            optimizer.zero_grad()

        # 更新教师模型（例如使用EMA）
        alpha = 0.99  # EMA系数
        for teacher_param, student_param in zip(teacher_model.parameters(), student_model.parameters()):
            teacher_param.data.mul_(alpha).add_(student_param.data, alpha=1 - alpha)

        # 验证和保存模型
        if not noval:
            results, maps, _ = validate.run(data_dict, batch_size=batch_size // WORLD_SIZE * 2, imgsz=opt.imgsz, half=amp, model=student_model, single_cls=single_cls, dataloader=val_loader, save_dir=save_dir, plots=False, callbacks=callbacks, compute_loss=compute_loss)

        scheduler.step()

    torch.cuda.empty_cache()
    return results


